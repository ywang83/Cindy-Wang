public class increment {
    public static void main (String args[]){
        int n =5;
        System.out.println(++n); //6
        System.out.println("");

        int m=5;
        System.out.println(m); //5
        System.out.println(m++);//5
        System.out.println(m);//6
        System.out.println(++m);//7
        System.out.println("");

        int k=5;
        ++k;
        System.out.println(k++);//6
        System.out.println(k);//7
        System.out.println("");




    }
}
